package Autoclicker;

import java.awt.Robot;

public class AutoClickerMouse {
	
	public static void main(String[] args) 
	{
		
	while(true) {
		try 
		{
		
			
			{
				Robot r = new Robot();
				
				r.mouseMove(1600, 915);
				
				
				try {
			         Thread.sleep(0);
			     } catch (InterruptedException ie)
			     {
			      
			     }
			}
		}
		
		
		
		
		
		
	
				
		catch (Exception e)
		
		{
			System.out.println(e);
		}
	}
}
	
}
		
